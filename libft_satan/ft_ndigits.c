#include "libft.h"

size_t	ft_ndigits(int n)
{
	int	aux;
	int	dig;

	aux = n;
	dig = 0;
	while (aux != 0)
	{
		dig++;
		aux = aux / 10;
	}
	if (n <= 0)
		dig++;
	return (dig);
}
