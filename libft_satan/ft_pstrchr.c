#include "libft.h"

size_t	ft_pstrchr(char **s, int c)
{
	size_t pos;

	if (!s || !(*s))
		return (0);
	pos = 0;
	while (**s != c && **s != '\0')
	{
		(*s)++;
		pos++;
	}
	return (pos);
}
